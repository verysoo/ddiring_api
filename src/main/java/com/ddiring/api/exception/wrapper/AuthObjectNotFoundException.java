package com.ddiring.api.exception.wrapper;

public class AuthObjectNotFoundException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public AuthObjectNotFoundException() {
		super();
	}

	public AuthObjectNotFoundException(String message, Throwable cause) {
		super(message, cause);
	}

	public AuthObjectNotFoundException(String message) {
		super(message);
	}

	public AuthObjectNotFoundException(Throwable cause) {
		super(cause);
	}
	
	
	
}










