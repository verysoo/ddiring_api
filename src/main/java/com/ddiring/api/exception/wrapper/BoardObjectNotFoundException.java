package com.ddiring.api.exception.wrapper;

public class BoardObjectNotFoundException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public BoardObjectNotFoundException() {
		super();
	}

	public BoardObjectNotFoundException(String message, Throwable cause) {
		super(message, cause);
	}

	public BoardObjectNotFoundException(String message) {
		super(message);
	}

	public BoardObjectNotFoundException(Throwable cause) {
		super(cause);
	}
	
	
	
}










